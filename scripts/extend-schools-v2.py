now = datetime.now(CST).isoformat()

for s in schools:
    if not s.get('group'):
        name = s['name']
        for kw in sorted(UNIQUE_MORE.keys(), key=len, reverse=True):
            if kw in name:
                s['group'] = UNIQUE_MORE[kw]
                group_new += 1
                break
    
    if not s.get('admissionRoutes'):
        name = s['name']
        stage = s['schoolStage']
        routes = []
        
        if stage in ('junior', 'complete'):
            for feeder_kw, targets in MORE_FEEDER.items():
                if feeder_kw in name:
                    for hs_name in targets:
                        for hs in schools:
                            if hs['schoolStage'] == 'senior_high' and hs_name in hs['name']:
                                routes.append({
                                    "high_school_id": hs['id'],
                                    "high_school_name": hs['name'],
                                    "type": "中考对口",
                                    "confidence": "medium"
                                })
                                break
        
        if stage == 'complete':
            base = name.replace("（初中部）", "").replace("(初中部)", "")
            if base != name:
                for hs in schools:
                    if hs['schoolStage'] == 'senior_high' and hs['id'] != s['id']:
                        if base in hs['name'] or hs['name'] in base:
                            if not any(r['high_school_id'] == hs['id'] for r in routes):
                                routes.append({
                                    "high_school_id": hs['id'],
                                    "high_school_name": hs['name'],
                                    "type": "校内直升",
                                    "confidence": "high"
                                })
                            break
        
        if routes:
            s['admissionRoutes'] = routes
            routes_new += 1
    
    s['updatedAt'] = now
    if s.get('profileDepth') == 'foundation' and (s.get('group') or s.get('admissionRoutes')):
        s['profileDepth'] = 'enhanced'

after_g = sum(1 for s in schools if s.get('group'))
