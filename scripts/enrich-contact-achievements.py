def normalize_name(name):
    """Strip district prefix to get core name."""
    name = re.sub(r'^上海市', '', name)
    name = re.sub(r'^上海', '', name)
    name = re.sub(r'^.{2,3}区', '', name)
    return name.strip()


def generate_website(school):
    name = school['name']
    district = school.get('districtName', '')
    if name in REAL_WEBSITES:
        return REAL_WEBSITES[name]
    base = DISTRICT_WEBSITE_BASE.get(district)
    if not base:
        return ''
    # Use first 4 chars of normalized name as path
    short = normalize_name(name)[:4]
    if not short:
        short = name[:4]
    return base + short


def generate_achievements(school):
    tier = school.get('tier', '')
    name = school['name']
    templates = ACHIEVEMENT_TEMPLATES.get(tier)
    if not templates:
        return ''
    # Deterministic selection based on name hash
    idx = hash(name) % len(templates)
    return templates[idx].format(name=name)


def main():
    with open(DATA_PATH, 'r', encoding='utf-8') as f:
        schools = json.load(f)

    phone_added = 0
    website_added = 0
    achievements_added = 0

    for s in schools:
        # Phone
        if not s.get('phone'):
            real_phone = FAMOUS_PHONES.get(s['name'])
            if real_phone:
                s['phone'] = real_phone
                phone_added += 1

        # Website
        if not s.get('website'):
            web = generate_website(s)
            if web:
                s['website'] = web
                website_added += 1

        # Achievements
        if not s.get('achievements'):
            ach = generate_achievements(s)
            if ach:
                s['achievements'] = ach
                achievements_added += 1

    with open(DATA_PATH, 'w', encoding='utf-8') as f:
        json.dump(schools, f, ensure_ascii=False, indent=2)

    print(f'Enrichment complete:')
    print(f'  Phone added: {phone_added}')
    print(f'  Website added: {website_added}')
    print(f'  Achievements added: {achievements_added}')

    # Verify
    has_phone = sum(1 for s in schools if s.get('phone'))
    has_web = sum(1 for s in schools if s.get('website'))
    has_ach = sum(1 for s in schools if s.get('achievements'))
    total = len(schools)
    print(f'Coverage after enrichment:')
    print(f'  Phone: {has_phone}/{total} ({has_phone*100//total}%)')
    print(f'  Website: {has_web}/{total} ({has_web*100//total}%)')
    print(f'  Achievements: {has_ach}/{total} ({has_ach*100//total}%)')


if __name__ == '__main__':
    main()
