def clean_existing_policies():
    """清理现有的低质量政策文件"""
    print("=" * 60)
    print("开始清理低质量政策文件")
    print("=" * 60)
    
    # 读取现有政策
    if os.path.exists(DATA_PATH):
        with open(DATA_PATH, 'r', encoding='utf-8') as f:
            existing_policies = json.load(f)
        print(f"现有政策文件数: {len(existing_policies)}")
    else:
        existing_policies = []
        print("未找到现有政策文件")
    
    # 质量评估
    quality_issues = {
        'no_content': [],
        'duplicate': [],
        'low_quality': []
    }
    
    valid_policies = []
    for p in existing_policies:
        # 检查内容是否为空
        if not p.get('content') or len(p.get('content', '')) < 50:
            quality_issues['no_content'].append(p['id'])
            continue
        
        # 检查是否重复
        if any(p['id'] == hp['id'] for hp in HIGH_QUALITY_POLICIES):
            quality_issues['duplicate'].append(p['id'])
            continue
        
        # 检查质量
        if len(p.get('summary', '')) < 20:
            quality_issues['low_quality'].append(p['id'])
            continue
        
        valid_policies.append(p)
    
    print(f"\n质量评估结果:")
    print(f"  无内容: {len(quality_issues['no_content'])}")
    print(f"  重复（将被新版本替换）: {len(quality_issues['duplicate'])}")
    print(f"  低质量: {len(quality_issues['low_quality'])}")
    print(f"  保留的有效政策: {len(valid_policies)}")
    
    # 合并新旧政策
    final_policies = valid_policies + HIGH_QUALITY_POLICIES
    print(f"\n合并后政策总数: {len(final_policies)}")
    
    # 保存清理后的政策
    with open(DATA_PATH, 'w', encoding='utf-8') as f:
        json.dump(final_policies, f, ensure_ascii=False, indent=2)
    
    print(f"✓ 已保存清理后的政策文件: {DATA_PATH}")
    
    return final_policies

