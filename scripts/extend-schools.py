def assign_group_extended(name):
    """扩展版集团分配"""
    # 长关键词优先
    for kw in sorted(UNIQUE_EXT_GROUPS.keys(), key=len, reverse=True):
        if kw in name:
            return UNIQUE_EXT_GROUPS[kw]
    return None

def gen_routes_extended(school, all_schools):
    """扩展版升学路线生成"""
    name = school['name']
    stage = school['schoolStage']
    routes = []
    
    if stage in ('junior', 'complete'):
        for feeder_kw, targets in EXTENDED_FEEDER.items():
            if feeder_kw in name:
                for hs_name in targets:
                    for hs in all_schools:
                        if hs['schoolStage'] == 'senior_high' and hs_name in hs['name']:
                            routes.append({
                                "high_school_id": hs['id'],
                                "high_school_name": hs['name'],
                                "type": "中考对口",
                                "confidence": "medium"
                            })
                            break
    
    # 完全中学内部直升
    if stage == 'complete':
        base = name.replace("（初中部）", "").replace("(初中部)", "")
        if base != name:
            for hs in all_schools:
                if hs['schoolStage'] == 'senior_high' and hs['id'] != school['id']:
                    if base in hs['name'] or hs['name'] in base:
                        if not any(r['high_school_id'] == hs['id'] for r in routes):
                            routes.append({
                                "high_school_id": hs['id'],
                                "high_school_name": hs['name'],
                                "type": "校内直升",
                                "confidence": "high"
                            })
                        break
    
    return routes

