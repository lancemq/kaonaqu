def print_batch_2_summary(policies):
    """打印第二批政策处理摘要"""
    print("\n" + "=" * 60)
    print("📊 第二批政策文件处理摘要")
    print("=" * 60)
    
    batch_2_ids = [p['id'] for p in BATCH_2_POLICIES]
    batch_2_policies = [p for p in policies if p['id'] in batch_2_ids]
    
    # 按类别统计
    categories = {}
    for p in batch_2_policies:
        cat = p.get('category', '未分类')
        if cat not in categories:
            categories[cat] = []
        categories[cat].append(p)
    
    print(f"\n第二批政策数: {len(batch_2_policies)}")
    print(f"政策类别数: {len(categories)}")
    
    for cat, polys in sorted(categories.items(), key=lambda x: -len(x[1])):
        print(f"\n{cat}: {len(polys)} 个政策")
        for p in polys:
            content_len = len(p.get('content', ''))
            print(f"  - {p['title'][:40]}... ({content_len}字)")
    
    # 重要性分布
    high_importance = [p for p in batch_2_policies if p.get('importance') == 'high']
    medium_importance = [p for p in batch_2_policies if p.get('importance') == 'medium']
    
    print(f"\n重要性分布:")
    print(f"  高重要性: {len(high_importance)} 个")
    print(f"  中重要性: {len(medium_importance)} 个")
    
    # 目标受众
    audiences = {}
    for p in batch_2_policies:
        aud = p.get('targetAudience', '')
        if aud:
            audiences[aud] = audiences.get(aud, 0) + 1
    
    print(f"\n目标受众:")
    for aud, count in sorted(audiences.items(), key=lambda x: -x[1]):
        print(f"  {aud}: {count} 个政策")

