function splitSentences(text) {
  return String(text || '')
    .split(/(?<=[。！？；])/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function uniqueItems(items) {
  return [...new Set(items.filter(Boolean))];
}

function normalizeSentence(text) {
  return String(text || '')
    .replace(/\s+/g, ' ')
    .replace(/^[\-•\d.\s]+/, '')
    .trim();
}

function extractStructuredPoints(text) {
  const normalized = String(text || '').replace(/\s+/g, ' ').trim();
  if (!normalized) return [];

  const matches = normalized.match(/[^。！？；]*?(?:\d{1,2}月\d{1,2}日(?:至\d{1,2}日)?(?:\d{1,2}:\d{2}(?:至|—|-)\d{1,2}:\d{2})?|[0-9]{1,2}:[0-9]{2}(?:至|—|-)[0-9]{1,2}:[0-9]{2}|不低于\d+分|总分\d+分|最多可[^，。；]*|须[^，。；]*|可[^，。；]*|安排在[^，。；]*|公布[^，。；]*|明确[^，。；]*|涵盖[^，。；]*|获得[^，。；]*|入选[^，。；]*)[^。！？；]*[。！？；]?/g);

  if (!matches) {
    return splitSentences(normalized).map(normalizeSentence);
  }

  return uniqueItems(matches.map(normalizeSentence).filter(Boolean));
}

function classifyDetailSection(item) {
  if (item.newsType === 'exam') {
    return item.examType === 'gaokao' || item.examType === 'zhongkao' ? '## 关键安排' : '## 重点信息';
  }
  if (item.newsType === 'admission') {
    return '## 重点规则';
  }
  if (item.newsType === 'school') {
    return '## 学校观察点';
  }
  return '## 主要内容';
}

