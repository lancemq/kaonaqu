export function buildNewsMarkdown(item) {
  if (curatedNewsMarkdown[item.id]) {
    return curatedNewsMarkdown[item.id]
      .replaceAll('{{SOURCE_NAME}}', item.source?.name || '官方来源')
      .replaceAll('{{SOURCE_URL}}', item.source?.url || '/news');
  }

  const sourceName = item.source?.name || '官方来源';
  const sourceUrl = item.source?.url || '';
  const summarySentences = splitSentences(item.summary);
  const contentSentences = splitSentences(item.content);
  const keyPoints = uniqueItems([
    ...extractStructuredPoints(item.summary),
    ...extractStructuredPoints(item.content),
    ...summarySentences.map(normalizeSentence),
    ...contentSentences.map(normalizeSentence)
  ]).slice(0, 6);
  const detailHeading = classifyDetailSection(item);

  const lines = [
    '## 新闻概览',
    item.summary || '暂无摘要。',
    '',
    '## 核心信息',
    `- 发布时间：${item.publishedAt || '暂无日期'}`,
    `- 新闻类别：${item.category || getNewsTypeLabel(item)}`,
    `- 适用考试线：${getExamLabel(item)}`,
    `- 信息来源：${sourceName}`,
    '',
    detailHeading,
    ...(keyPoints.length ? keyPoints.map((point) => `- ${point}`) : ['- 暂无详细要点。']),
    '',
    '## 这条信息为什么值得看',
    getImportanceText(item, keyPoints),
    '',
    '## 适合谁先看',
    getAudience(item),
    '',
    '## 阅读提示',
    getReadingTip(item),
    '',
    '## 官方原文',
    sourceUrl ? `- [查看${sourceName}原文](${sourceUrl})` : '- 暂无原文链接'
  ];

  return lines.join('\n');
}
